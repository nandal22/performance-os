import type { Equipment, ExerciseCategory } from '@/types';

export type PlanPhase = 'warmup' | 'workout' | 'stretch' | 'recovery';

export interface PlanMedia {
  kind: 'image' | 'gif' | 'video';
  url: string;
  source: string;
  sourceUrl: string;
}

export interface PlanItem {
  id: string;
  name: string;
  target: string;
  cue: string;
  media?: PlanMedia;
}

export interface PlanExercise extends PlanItem {
  sets: number;
  repRange: string;
  logUnit?: 'reps' | 'seconds';
  dbName: string;
  aliases: string[];
  category: ExerciseCategory;
  primaryMuscle: string;
  secondaryMuscles: string[];
  equipment: Equipment;
}

export interface WorkoutPlanDay {
  day: number;
  shortLabel: string;
  title: string;
  focus: string;
  duration: string;
  accent: string;
  warmup: PlanItem[];
  workout: PlanExercise[];
  stretch: PlanItem[];
  recovery?: PlanItem[];
}

